package com.hi.module.self.model;

/**
 * @author MM_Zerui
 * 封装活动信息的接收Bean
 */
public class RecvActivityBean {
	private String buildTimes;
	private String canover;
	private String head;
	private String id;
	private String inviteTimes;
	private String isjoin;
	private String joinTimes;
	private String mid;
	private String remark;
	public String getBuildTimes() {
		return buildTimes;
	}
	public void setBuildTimes(String buildTimes) {
		this.buildTimes = buildTimes;
	}
	public String getCanover() {
		return canover;
	}
	public void setCanover(String canover) {
		this.canover = canover;
	}
	public String getHead() {
		return head;
	}
	public void setHead(String head) {
		this.head = head;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getInviteTimes() {
		return inviteTimes;
	}
	public void setInviteTimes(String inviteTimes) {
		this.inviteTimes = inviteTimes;
	}
	public String getIsjoin() {
		return isjoin;
	}
	public void setIsjoin(String isjoin) {
		this.isjoin = isjoin;
	}
	public String getJoinTimes() {
		return joinTimes;
	}
	public void setJoinTimes(String joinTimes) {
		this.joinTimes = joinTimes;
	}
	public String getMid() {
		return mid;
	}
	public void setMid(String mid) {
		this.mid = mid;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}
}
