package com.hi.module.locale.model;

/**
 * @author MM_Zerui 封装现场状态的Bean
 */
public class LocalStateBean {
	private String uid;
	private String t;
	private String c;

	public String getUid() {
		return uid;
	}

	public void setUid(String uid) {
		this.uid = uid;
	}

	public String getT() {
		return t;
	}

	public void setT(String t) {
		this.t = t;
	}

	public String getC() {
		return c;
	}

	public void setC(String c) {
		this.c = c;
	}

}
